local M = {}

local function load_npm_packages(name, callback)
  if not name then
    callback({})
    return
  end

  vim.system(
    {
      "npm",
      "search",
      "--json",
      "--no-update-notifier",
      name,
    },
    nil,
    function(result)
      if result.code ~= 0 then
        callback({})
        return
      end

      ---@type NpmPackage[]
      local npm_items = vim.json.decode(result.stdout)
      return callback(npm_items)
    end
  )
end

---@param name string
---@param callback fun(version:string)
local function list_latest_versions(name, callback)
  if not name then
    callback("")
    return
  end

  vim.system(
    {
      "npm",
      "info",
      name,
      "version",
      "--no-update-notifier",
    },
    nil,
    function(result)
      if result.code ~= 0 then
        callback("")
        return
      end

      local lines = vim.split(result.stdout, "\n")
      local version = lines[1]
      if not version then
        return callback("")
      end
      return callback(version)
    end
  )
end

---@param name string
---@param callback fun(version:string[])
local function list_all_versions(name, callback)
  if not name then
    callback({})
    return
  end

  vim.system(
    {
      "npm",
      "info",
      name,
      "versions",
      "--json",
      "--no-update-notifier",
    },
    nil,
    function(result)
      if result.code ~= 0 then
        callback({})
        return
      end

      local versions = vim.json.decode(result.stdout)
      if not versions then
        return callback({})
      end
      return callback(versions)
    end
  )
end

M.load_npm_packages = load_npm_packages
M.list_latest_versions = list_latest_versions
M.list_all_versions = list_all_versions

return M
